const tabs = document.querySelectorAll(".tab");
const columnsContainer = document.querySelector(".footer-top");

let currentCategoryData = [];
const initialItemsToShow = 17;
const seeMoreLink = document.createElement("a");
seeMoreLink.classList.add("see-more-link");
seeMoreLink.textContent = "See More";
seeMoreLink.href = "#"; // Prevent default action
seeMoreLink.style.display = "none";
seeMoreLink.style.textDecoration = "none"; // Style link to look like plain text
seeMoreLink.style.color = "black"; // Set color to black
seeMoreLink.style.marginTop = "10px"; // Add margin on top
columnsContainer.appendChild(seeMoreLink);

function renderItems(startIndex, endIndex) {
  // Clear previous content except "See More" link
  columnsContainer
    .querySelectorAll(".column")
    .forEach((column) => column.remove());

  // Populate columns with new data
  const categoryData = currentCategoryData.slice(startIndex, endIndex);

  categoryData.forEach((item, index) => {
    const columnIndex = index % 6;
    let column = columnsContainer.querySelector(
      `.column[data-index="${columnIndex}"]`
    );

    if (!column) {
      column = document.createElement("div");
      column.classList.add("column");
      column.setAttribute("data-index", columnIndex);
      columnsContainer.appendChild(column);
    }

    const p = document.createElement("p");
    p.style.marginBottom = "10px"; // Add margin at the bottom
    p.innerHTML = `<a href="${item.name
      .replace(/\s+/g, "")
      .toLowerCase()}.html" target="_blank"><strong>${
      item.name
    }</strong><span>${item.category}</span></a>`;

    const link = p.querySelector("a");
    if (link) {
      link.style.textDecoration = "none"; // Style links to look like plain text
      link.style.color = "inherit"; // Use inherited color
    }

    column.appendChild(p);
  });

  // Append "See More" link after the last item
  columnsContainer.appendChild(seeMoreLink);
}

tabs.forEach((tab) => {
  tab.addEventListener("click", () => {
    // Remove active class from all tabs
    tabs.forEach((t) => t.classList.remove("active"));
    tab.classList.add("active");

    // Get the data category
    let category = tab.getAttribute("data-category");

    // Set the current category data
    currentCategoryData = data[category];
    seeMoreLink.style.display =
      currentCategoryData.length > initialItemsToShow ? "block" : "none";

    // Render the initial set of items
    renderItems(0, initialItemsToShow);
  });
});

// Add click event for the "See More" link
seeMoreLink.addEventListener("click", (event) => {
  event.preventDefault(); // Prevent default link action

  // Show all remaining items
  renderItems(initialItemsToShow, currentCategoryData.length);

  // Hide the "See More" link after all items are displayed
  seeMoreLink.style.display = "none";
});

// Trigger click on the first tab to display default content
tabs[0].click();

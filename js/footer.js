const tabs = document.querySelectorAll(".tab");
const columnsContainer = document.querySelector(".footer-top");

tabs.forEach((tab) => {
  tab.addEventListener("click", () => {
    // Remove active class from all tabs
    tabs.forEach((t) => t.classList.remove("active"));
    tab.classList.add("active");

    // Get the data category and adjust for keys
    let category = tab.getAttribute("data-category");
    // category = category.toLowerCase().replace(/ & /g, "And").replace(/ /g, "");

    // Clear previous content
    columnsContainer.innerHTML = "";

    // Populate columns with new data
    const categoryData = data[category];

    if (categoryData) {
      categoryData.forEach((item, index) => {
        const columnIndex = index % 6;
        let column = columnsContainer.querySelector(
          `.column[data-index="${columnIndex}"]`
        );

        if (!column) {
          column = document.createElement("div");
          column.classList.add("column");
          column.setAttribute("data-index", columnIndex);
          columnsContainer.appendChild(column);
        }

        const p = document.createElement("p");
        p.innerHTML = `<strong>${item.name}</strong><span>${item.category}</span>`;
        column.appendChild(p);
      });
    }
  });
});

// Trigger click on the first tab to display default content
tabs[0].click();
